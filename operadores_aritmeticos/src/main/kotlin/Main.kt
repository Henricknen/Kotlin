fun main(args: Array<String>) {

    var numero1 = 20
    var numero2 = 10

    println(numero1 + numero2)      // somando as variáveis ultilizando operador '+'
    println("---")
    println(numero1 - numero2)          // subtraindo '-'
    println("---")
    println(numero1 / numero2)      // divisão '/'
    println("---")
    println(numero1 * numero2)          // multiplicando '*'
    println("---")
    println(numero1 % numero2)      // resto da divisão '%'
    println("---")
    println(++numero1)      // ultilização do operador de incremento '++'
    println("---")
    println(--numero2)      // ultilização do operador de decremento '--'
}
