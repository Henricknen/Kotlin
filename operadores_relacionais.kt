fun main(args: Array<String>) {

    var valor1 = 20
    var valor2 = 60

    println(valor1 == valor2)       // 'igualdade' 
    println("---")
    println(valor1 < valor2)    // 'menor'
    println("---")
    println(valor1 > valor2)        // 'maior'
    println("---")
    println(valor1 <= valor2)   // 'menor ou igual'
    println("---")
    println(valor1 >= valor2)       // 'maior ou igual'
    println("---")
    println(valor1 != valor2)   // 'diferente'
}
