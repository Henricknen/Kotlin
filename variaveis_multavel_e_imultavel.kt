fun main(args: Array<String>) {     // 'args' passa os argumentos
    println("Trabalhando com variáveis...")   // 'println' é uma função

    var nome = "Luis Henrique Silva Ferreira"   // se ultiliza 'var' para se criar uma variável multável ou seja pode ser alterada
    nome = "FullStack"      // 'alteração' da variável nome

    val idade = 32       // 'val' define a variável como imultável não pode ser alterada

    println("Programador: " + nome)
    println ("No momento desta codificação tenho: " + idade)
}